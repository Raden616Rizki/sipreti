class Unit_kerja_model extends CI_Model
{

	public $table = 'unit_kerja';
	public $id = 'id_unit_kerja';
	public $order = 'DESC';

	function __construct()
	{
		parent::__construct();
	}

	// get all
	function get_all()
	{
		$this->db->where('(deleted_at IS NULL OR deleted_at = "")');
		$this->db->order_by($this->id, $this->order);
		return $this->db->get($this->table)->result();
	}

	// get data by id
	function get_by_id($id)
	{
		$this->db->where($this->id, $id);
		return $this->db->get($this->table)->row();
	}

	// get total rows
	function total_rows($q = NULL, $onlyActive = FALSE)
	{
		$this->db->like('nama_unit_kerja', $q);
		if ($onlyActive) {
			$this->db->where('(deleted_at IS NULL OR deleted_at = "")');
		}
		return $this->db->count_all_results($this->table);
	}

	// get data with limit and search
	public function get_limit_data($limit, $start = 0, $q = NULL, $onlyActive = FALSE)
	{
		$this->db->select('unit_kerja.*, radius_absen.ukuran');
		$this->db->from($this->table);
		$this->db->join('radius_absen', 'radius_absen.id_radius = unit_kerja.id_radius', 'left');

		if (!empty($q)) {
			$this->db->like('unit_kerja.nama_unit_kerja', $q);
		}

		if ($onlyActive) {
			$this->db->where('(unit_kerja.deleted_at IS NULL OR unit_kerja.deleted_at = "")');
		}
		$this->db->order_by('unit_kerja.nama_unit_kerja', 'ASC');
		$this->db->limit($limit, $start);
		return $this->db->get()->result();
	}

	// insert data
	function insert($data)
	{
		$this->db->insert($this->table, $data);
	}

	// update data
